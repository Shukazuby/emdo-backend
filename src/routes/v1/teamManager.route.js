const express = require('express')
const {teamManagerController, employerController, authController} = require('../../controllers/')
const auth = require('../../middlewares/auth');


const router = express.Router()
router.post('/add', auth(), teamManagerController.addNewUser);
router.patch('/:id', teamManagerController.updateNewUser)
router.post('/login-standard', authController.loginStandard);
router.post('/login-admin', authController.loginAdmin);



module.exports = router

